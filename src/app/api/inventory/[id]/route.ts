import { NextRequest, NextResponse } from 'next/server';
import axios from 'axios';
import { getInventoryItemById, updateInventoryItem, deleteInventoryItem } from '@/lib/dataverse/inventory';
import { serverError, withSchool } from '@/lib/api-guard';

export async function GET(_req: NextRequest, { params }: { params: Promise<{ id: string }> }) {
    return withSchool(_req, async () => {
        try {
            const { id } = await params;
            const item = await getInventoryItemById(id);
            return NextResponse.json({ success: true, data: item });
        } catch (error: unknown) {
            if (axios.isAxiosError(error) && error.response?.status === 404) {
                return NextResponse.json({ success: false, error: 'Inventory item not found' }, { status: 404 });
            }
            return serverError(error);
        }
    });
}

export async function PUT(request: NextRequest, { params }: { params: Promise<{ id: string }> }) {
    return withSchool(request, async () => {
        try {
            const { id } = await params;
            const body = await request.json();
            const item = await updateInventoryItem(id, body);
            return NextResponse.json({ success: true, data: item });
        } catch (error: unknown) {
            if (axios.isAxiosError(error) && error.response?.status === 404) {
                return NextResponse.json({ success: false, error: 'Inventory item not found' }, { status: 404 });
            }
            return serverError(error);
        }
    });
}

export async function DELETE(_req: NextRequest, { params }: { params: Promise<{ id: string }> }) {
    return withSchool(_req, async () => {
        try {
            const { id } = await params;
            await deleteInventoryItem(id);
            return NextResponse.json({ success: true });
        } catch (error: unknown) {
            if (axios.isAxiosError(error) && error.response?.status === 404) {
                return NextResponse.json({ success: false, error: 'Inventory item not found' }, { status: 404 });
            }
            return serverError(error);
        }
    });
}
